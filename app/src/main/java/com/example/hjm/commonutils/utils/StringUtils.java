    public static int matchChar(String Str, char ch){
        if(TextUtils.isEmpty(Str)) return 0;
        int size=0;
        for(char chs:Str.toCharArray()){
            if(chs==ch)
                size++;
        }
        return size;
    }

    public static String formatCard(String cardStr){
        StringBuffer buffer=new StringBuffer();
        for(int index=0;index<cardStr.length();index++){
            if(index<=3||index>cardStr.length()-5){
                buffer.append(cardStr.charAt(index));
            }else{
                buffer.append("*");
            }

            if((index+1)%4==0&&index+1!=cardStr.length()){
                buffer.append(" ");
            }
        }
        return buffer.toString();
    }

    public static String formatCardGap(String cardStr){
        StringBuffer buffer=new StringBuffer();
        for(int index=0;index<cardStr.length();index++){
            buffer.append(cardStr.charAt(index));
            if((index+1)%4==0&&index+1!=cardStr.length()){
                buffer.append(" ");
            }
        }
        return buffer.toString();
    }

    public static String formatPhone(String phoneStr){
        if(TextUtils.isEmpty(phoneStr)) return null;
        StringBuffer buffer=new StringBuffer();
        for(int index=0;index<phoneStr.length();index++){
            if(index<4){
                buffer.append(phoneStr.charAt(index));
            }else{
                buffer.append("*");
            }
        }
        return buffer.toString();
    }

    public static String formatWeChat(String weChatStr){
        if(TextUtils.isEmpty(weChatStr)) return null;
        StringBuffer buffer=new StringBuffer();
        for(int index=0;index<weChatStr.length();index++){
            if(index<4&&weChatStr.length()>4){
                buffer.append(weChatStr.charAt(index));
            }else{
                buffer.append("*");
            }
        }
        return buffer.toString();
    }








